    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        

        max = Integer.parseInt(txtSimulaciones.getText());
        if (txtSimulaciones.getText().trim().isEmpty() || max < 100 || (max * 3) >= norm) {
            JOptionPane.showInputDialog(null, "Datos incorrectos", "Informaci√≥n", JOptionPane.ERROR_MESSAGE);
            txtProb.setText("");
            txtSimulaciones.setText("");
            jTextArea1.setText("");
        } else {
            for (int i = 1; i <= max; i++) {

                jTextArea1.append("Ensayo No. " + i);
                jTextArea1.append("\n");
                for (int j = 1; j <= 3; j++) {
                    cont++;
                    sem = (mult * sem + sum) % norm;
                    rnd = (double) sem / norm;
                    truncar = (rnd * 5.0) + 1.0d;
                    Rec[j] = Integer.parseInt("" + String.valueOf(truncar).charAt(0));
                    jTextArea1.append("  " + cont + "   Pelota en el recipiente No: " + Rec[j]);
                    jTextArea1.append("\n");
                }
                if (Rec[1] != Rec[2] && Rec[3] != Rec[2] && Rec[3] != Rec[1]) {
                    prob = prob + 1;
                    jTextArea1.append("Cumple");
                    jTextArea1.append("\n\n");
                } else {
                    jTextArea1.append("No Cumple");
                    jTextArea1.append("\n\n");
                 
                }
            }
            tol = (double) prob / max;
            txtProb.setText(String.valueOf(tol));
        }

    }//GEN-LAST:event_btnGenerarActionPerformed

